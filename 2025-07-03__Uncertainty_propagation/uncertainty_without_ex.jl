# ╔═╡ 41997a8d-b805-4023-be46-90a459e87e7c
md"The core idea of `MonteCarloMeasurements.jl` (MCM.jl) for propagating uncertainties is completely different compared to `Measurements.jl`. In this case, it uses **Monte-Carlo methods** by generating `Particles` following a distribution.

However, while the method is quite different, the syntax is actually quite similar (so similar that there is a conflict if you use both packages at the same time..). 

Let's reproduce our first example:"

# ╔═╡ 01721d57-75c2-456b-ac79-c7a6196b709f
begin 
	a_MC = 10 ± 0.2
	b_MC = 5 ± 0.1
	c_MC = a_MC + b_MC
	@show c
	@show c_MC
end

# ╔═╡ c9aea9c2-b6ba-43d2-bcac-24e227409a37
