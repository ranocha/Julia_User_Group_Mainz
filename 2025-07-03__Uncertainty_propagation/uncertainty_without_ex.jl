md"So it seems that both methods are consistent. However, there are cases where they will provide different answers. This is because of the limit of linear error propagation: if we are using non-linear functions with \"large\" uncertainties, this can give very approximate results at best.

For instance, if we look at the functions `sin()` and `exp()` and compare the results between Measurements.jl and MCM.jl, the differences are seen. On which part of the functions do they appear?
"

# ╔═╡ 4352b892-9bf0-48fa-a4e0-0a328c634b36
md"""
uncert = $(@bind uncert Slider(0.0:0.01:1.0, show_value=true))
