end

# ╔═╡ 7abb9f8e-d971-451a-922d-be107ea56503
md"
Let's check quickly that the uncertainty value is correct. 
In linear error propagation, we can propagate errors using this general formula (for uncorrelated values):
"

# ╔═╡ 0ae379cb-b7a4-4f06-b99c-e668681fc666
md"$\sigma_f \approx \sqrt{ \sum_{i=1}^n \left( \frac{\partial f}{\partial x_i} \cdot \sigma_{x_i} \right)^2 }$"

# ╔═╡ e1abef49-242e-4cf1-846a-49ded5a027f0
begin
	∂c_∂a = 1.0  # partial derivative of c with respect to a
	∂c_∂b = 1.0  # partial derivative of c with respect to b
	σc = sqrt((∂c_∂a * a.err)^2 + (∂c_∂b * b.err)^2)
end

# ╔═╡ a40c359a-b174-4141-9569-52a3ed072548
σc == c.err

# ╔═╡ 4d8bbd9d-92e6-4c31-9d11-e62dcc069c4e
md"
Let's compute and propagate the uncertainties for the period `T` of a pendulum: 

$T = 2\pi \sqrt{ \frac{L}{g } }$
where $L$ is the length of the pendulum, and $g$ is the gravitational acceleration.

We can also use `Unitful.jl` easily to include units.
"

# ╔═╡ 27620a83-4090-4dd5-b49f-fb5862f97cc2
begin
	L = (measurement(400, 1))u"cm"  # cm
	g = (measurement(9.80665, 0.00001))u"m/s^2"  # m/s²

	T = upreferred(2π * sqrt(L / (g)))
end

# ╔═╡ ebff23ed-df27-4c2d-9862-d986aa11262d
md"
We can check again the results:
"

# ╔═╡ 0856e4af-daa3-4743-80ca-0be218d4d0cb
begin
	L_val = value(L)  
	g_val = value(g)  
	σL = uncertainty(L)  
	σg = uncertainty(g)  

	# Compute partial derivatives manually
	∂T_∂L = π / sqrt(L_val * g_val)
	∂T_∂g = -π * sqrt(L_val) / (g_val^(3/2))

	# Compute propagated uncertainty using linear error propagation
	σT = upreferred(sqrt((∂T_∂L * σL)^2 + (∂T_∂g * σg)^2))

	# print sensitivity-weighted uncertainty contribution
	println(upreferred(abs(∂T_∂L* σL)))
	println(upreferred(abs(∂T_∂g*σg)))
end

# ╔═╡ 2a124e83-1124-4f81-895e-7d34ea517b20
uncertainty(T) == σT

# ╔═╡ f80c288d-162a-40f2-9fea-6b067f94dca2
md"
This is exactly how `Measurements.jl` is working under the hood except that partial derivatives are calculated automatically using operator overloads, so no need to compute them by hand, like here!

We can check that the values are the same using `uncertainty_components`:
"

# ╔═╡ 368f61b5-3a4f-4ef8-8178-e6205152ce6f
begin
	T_without_unit = T |> ustrip
	uncertainty_components(T_without_unit)
end

# ╔═╡ 655b4c25-f933-458b-a0e1-6fd23438b223
md"
This yields exactly the product of the partial derivatives with their corresponding uncertainties that we previously calculated by hand. These values are important because they quantify the contribution of each variable’s uncertainty to the overall uncertainty.

This can be used in general as an indicator for which variables we could focus on to reduce the final uncertainty.

So with that, it seems that we have a robust way to propagate uncertainties from any type of input, but is it the case? Let's test that by comparing the results with `MonteCarloMeasurements.jl`."

