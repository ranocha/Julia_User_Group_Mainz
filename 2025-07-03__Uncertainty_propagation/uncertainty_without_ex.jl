# ╔═╡ 59846317-9a5f-4520-aa06-e1d7416c41d9
begin
    # Parameters
    x_nominal = range(-2π, 2π; length=200)
    x = measurement.(x_nominal, uncert)
    x_MC = x_nominal .± uncert

    ## Compute sin(x)
    y_sin = sin.(x)
    y_sin_MC = sin.(x_MC)
    y_sin_vals = value.(y_sin)
    y_sin_errs = uncertainty.(y_sin)

    ## Compute exp(x)
    y_exp = exp.(x)
    y_exp_MC = exp.(x_MC)
    y_exp_vals = value.(y_exp)
    y_exp_errs = uncertainty.(y_exp)

    ## Create figure with 2 subplots
    plt1 = ribbonplot(x_nominal, y_sin_MC, q=0.16, label="", title="sin(x ± $uncert)", legend=:bottomleft)
    plot!(x_nominal, y_sin_vals, ribbon=1.96*y_sin_errs, label="", lw=2, color=:red)

    plt2 = ribbonplot(x_nominal, y_exp_MC, q=0.16, label="MCM.jl", title="exp(x ± $uncert)", legend=:topleft, xlims=(0,5), ylims=(-Inf,400))
    plot!(x_nominal, y_exp_vals, ribbon=1.96*y_exp_errs, label="Measurements.jl", lw=2, color=:red)

    ## Combine plots in a 1×2 layout
    plot(plt1, plt2, layout=(1, 2), size=(1000, 400))
end

# ╔═╡ 5c9dbad5-a53b-48b2-92ec-6b0458e50766
md"""
!!! important
    MCM.jl is able to perform non-linear uncertainty propagation, whereas Measurements.jl is restricted by the linear approximation. So, if there is a discrepency between the 2 packages, better to trust MCM.jl! This should be the case only if the uncertainties are "big enough", otherwise, the results should be equivalent. 

	However, MCM.jl is slower compared to Measurements.jl, because it is particle-based. No free cake!
"""

