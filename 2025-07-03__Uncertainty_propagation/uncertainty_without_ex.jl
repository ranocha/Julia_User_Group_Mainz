# ╔═╡ c3957d0b-0e07-4871-8761-bc74490666fd
md"""
!!! note
    Because MCM.jl is generating particles randomly, the calculated uncertainty will vary slightly if you run the previous cell multiple times.
	
	Also, here, it is assuming a normal distribution for the generation of the particles, but it doesn't have to be, contrary to [Measurements.jl](https://juliaphysics.github.io/Measurements.jl/stable/)! You are free to use another distribution, using the package [Distributions.jl](https://juliastats.org/Distributions.jl/stable/) for instance."""

# ╔═╡ 94a18cd6-0fca-4c8a-8071-a1d61bfc85e5
