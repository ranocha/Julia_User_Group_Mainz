# ╔═╡ 0dc1ed62-6a78-4f42-b590-15998b6f0812
md"""##### Downloading airfoil data
The following function downloads the data from the AirfoilTools database and prepare the data as requested by HOHQMesh.
"""

# ╔═╡ b516a4cb-4cd8-48a1-b983-1570de0f6067
function load_airfoil(airfoil_name::String)
    url = "http://airfoiltools.com/airfoil/seligdatfile?airfoil=$(airfoil_name)"
    
    response = HTTP.get(url)

    righe = split(String(response.body), '\n')

    airfoil = []

    for riga in righe
        colonne = split(riga)
        if length(colonne) == 2
            push!(airfoil, (parse(Float64, colonne[1]), parse(Float64, colonne[2])))
        end
    end

    airfoil_coordinates = hcat([x[1] for x in airfoil], [y[2] for y in airfoil])

    x = airfoil_coordinates[:, 1]
    y = airfoil_coordinates[:, 2]

    if x[1] != x[end] || y[1] != y[end]
        x = vcat(x, x[1])
        y = vcat(y, y[1])
    end

    t = [0.0]
    for i in 2:length(x)
        dx = x[i] - x[i-1]
        dy = y[i] - y[i-1]
        distance = sqrt(dx^2 + dy^2)
        push!(t, t[end] + distance)
    end
    
    t .= t ./ t[end]
    
    result = hcat(t, x, y, zeros(length(x)))
    return result

end;

