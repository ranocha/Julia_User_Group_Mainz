md"""# Spline Curves and symmetry
In this example we define a symmetric mesh using the Batman logo. A spline data has to be given in a matrix structure of four columns, as shown below:

```math
\begin{array}{|c|c|c|c|}
\hline
\text{t (Curvilinear Abscissa)} & \text{X Coordinates} & \text{Y Coordinates} & \text{Z Coordinates} \\
\hline
\vdots & \vdots & \vdots & \vdots \\
\vdots & \vdots & \vdots & \vdots \\
\vdots & \vdots & \vdots & \vdots \\
\hline
\end{array}
```
where $$t \in [0,1]$$
"""

# ╔═╡ 82a3b026-9a25-4978-8170-b00f7abfd8db
md"""### Batman Logo's Spline
The spline will return as mentioned above the 4 ordered values for each value of the abscissa $$t$$.
"""

# ╔═╡ a4b731d2-b438-478b-aa7a-e0c9eb513e13
function spline_batman(t)
    x =
        -0.5 * t * 21 + 0.25 * abs(t * 21 - 1) - 0.5 * abs(t * 21 - 3) + 0.75 * abs(t * 21 - 5) + 1.5 * abs(t * 21 - 13) - 0.25 * abs(t * 21 - 17) - 1.25 * abs(t * 21 - 21) - 7 * sin((pi / 12) * (abs(t * 21 - 5) - abs(t * 21 - 8) + 3)) -
        (1 / 100) * (abs(t * 21 - 8) - abs(t * 21 - 13) - 5)^3 - 1.5
    y =
        3 / 4 * abs(t * 21 - 1) - (3 / 4) * abs(t * 21 - 3) - (7 / 5) * abs(t * 21 - 8) + (7 / 5) * abs(t * 21 - 13) + (7 / 16) * (abs(t * 21 - 3) - abs(t * 21 - 5) - 2)^2 + 4 * sin((pi / 12) * (abs(t * 21 - 5) - abs(t * 21 - 8) - 3)) -
        (5 / 16) * (abs(t * 21 - 13) - abs(t * 21 - 17))^2 - (1 / 4) * (abs(t * 21 - 17) - abs(t * 21 - 21) + 2)^2 + 11.5
    return [t x y 0]
end;

